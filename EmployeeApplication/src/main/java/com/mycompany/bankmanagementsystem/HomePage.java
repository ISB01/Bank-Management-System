    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try ( Connection conn = DriverManager.getConnection(url, uid, pw);  Statement stmt = conn.createStatement()) {

            String qry = "SELECT cust_id, CONCAT(cust_fname, ' ',cust_lname), cust_gender, cust_address, cust_phonenumber, cust_email"
            + " FROM bankcustomer";

            // Result set get the result of the SQL query
            ResultSet rs = stmt.executeQuery(qry);
            ResultSetMetaData rsmd = rs.getMetaData();
            int c = rsmd.getColumnCount();
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("ID");
            dtm.addColumn("Name");
            dtm.addColumn("Gender");
            dtm.addColumn("Address");
            dtm.addColumn("Phone");
            dtm.addColumn("Email");
            Object[] row;
            while (rs.next()) {
                row = new Object[c];
                for (int i = 0; i < c; i++) {
                    row[i] = rs.getString(i + 1);
                }
                dtm.addRow(row);
            }
            jTable1.setModel(dtm);
        } catch (SQLException ex) {
            System.err.println("SQLException: " + ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

